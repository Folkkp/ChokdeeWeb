function luckyNumbers(number) {
  const twoDigit = String(number).padStart(2, '0');
  const reversed = twoDigit.split('').reverse().join('');
  return `${twoDigit}, ${reversed}, ${number}${twoDigit.slice(-1)}`;
}

async function seedTempleFortunes() {
  const fortuneRows = temples.flatMap((temple) =>
    fortuneTextsByTemple[temple.id].map((text, index) => ({
      temple_id: temple.id,
      number: index + 1,
      text: `ใบที่ ${index + 1} ${text}`,
      lucky_numbers: luckyNumbers(index + 1),
      source_url: temple.source_url,
    }))
  );

  console.log(`Found ${temples.length} temples and ${fortuneRows.length} fortune sticks.`);

  const { error: templeError } = await supabase
    .from('fortune_temples')
    .upsert(temples, { onConflict: 'id' });

  if (templeError) {
    throw templeError;
  }

  const { error: fortuneError } = await supabase
    .from('fortune_sticks')
    .upsert(fortuneRows, { onConflict: 'temple_id,number' });

  if (fortuneError) {
    throw fortuneError;
  }

  console.log('Successfully seeded temple fortune data to Supabase.');
}

seedTempleFortunes().catch((error) => {
  console.error('Error seeding temple fortune data:', error.message);
  console.error('Make sure you have run the fortune_temples and fortune_sticks SQL migration first.');
  process.exit(1);
});
