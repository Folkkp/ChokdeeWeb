function getLuckyNumbers(number) {
  const padded = String(number).padStart(2, '0');
  const reversed = padded.split('').reverse().join('');
  return `${padded}, ${reversed}, ${number}${padded.slice(-1)}`;
}

export function getTempleById(templeId) {
  return fortuneTemples.find((temple) => temple.id === templeId) || fortuneTemples[0];
}

export function getFortunesForTemple(templeId) {
  const temple = getTempleById(templeId);
  const tone = templeTones[temple.id];

  return fortuneMeanings.map((meaning, index) => {
    const number = index + 1;

    return {
      num: number,
      templeId: temple.id,
      templeName: temple.name,
      text: `ใบที่ ${number} ${meaning} ${tone}`,
      lucky: getLuckyNumbers(number),
    };
  });
}
